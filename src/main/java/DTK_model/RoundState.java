package DTK_model;

/**
 * This class is used to store the current state of the game.
 */
public enum RoundState {
    /**
     * The game is in the first state.
     */
    TRAINING,
    /**
     * The game is in the second state.
     */
    BUILDING,
    /**
     * The game is in the third state.
     */
    ATTACKING
}
